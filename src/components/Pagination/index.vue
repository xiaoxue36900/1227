<template>
  <div class="pagination">
    <button>上一页</button>
    <button>1</button>
    <button disabled>···</button>

    <button>3</button>
    <button>4</button>
    <button>5</button>
    <button>6</button>
    <button>7</button>

    <button disabled>···</button>
    <button>{{ totalPages }}</button>
    <button>上一页</button>

    <button style="margin-left: 30px;" disabled>共 {{ total }} 条</button>
  </div>
</template>

<script>
export default {
  name: "Pagination",
  props: {
    currentPage: {
      type: Number,
      default: 1,
    },
    pageSize: {
      type: Number,
      default: 10,
    },
    // total: 总数量
    total: {
      type: Number,
      default: 0,
    },
    // showPageNo: 连续数码数
    showPageNo: {
      type: Number,
      default: 5,
    },
  },
  data() {
    return {
      // 将外部传入的当前页码作为当前组件的当前面页码初始值
      myCurrentPage: this.currentPage,
    };
  },
  computed: {
    totalPages() {
      const { total, pageSize } = this;
      return Math.ceil(total / pageSize);
    },
  },
};
</script>

